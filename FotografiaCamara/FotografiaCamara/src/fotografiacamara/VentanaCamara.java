    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        int pregunta= JOptionPane.showConfirmDialog(this, "Â¿Guardar foto?","pregunta", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (pregunta==0){
            File salidaImagen = new File("C:\\Users\\MELISSA\\Documents\\CamaraJava.jpg");
           try{
               ImageIO.write(ruta, "jpg", salidaImagen);
               JOptionPane.showMessageDialog(this, "Imagen Guardada en :"+ salidaImagen.getAbsolutePath());
           }catch(Exception e){
               System.out.println("Error "+e);
           }
        }
        lbFoto.setIcon(null);
        //btnSave
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnTomarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTomarActionPerformed
        btnSave.setEnabled(true);
        ImageIcon foto;
        foto = new ImageIcon(webcam.getImage());
        Icon iconoFoto = new ImageIcon(foto.getImage().getScaledInstance(lbFoto.getWidth(), lbFoto.getHeight(), Image.SCALE_SMOOTH));
        lbFoto.setIcon(iconoFoto);
        ruta = webcam.getImage();
    }//GEN-LAST:event_btnTomarActionPerformed

