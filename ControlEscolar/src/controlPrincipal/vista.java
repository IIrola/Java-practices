    private void btRegisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRegisActionPerformed
        cali1=Float.parseFloat(cal1.getText());
        cali2=Float.parseFloat(cal2.getText());
        cali3=Float.parseFloat(cal3.getText());
        //cali4=Float.parseFloat(cal4.getText());
        
        prom = (cali1+cali3+cali2/*+cali4*/)/3;
        
        Alumno alm = new Alumno(controlNum.getText() ,Nombre.getText(), cali1, cali2, cali3, /*cali4,*/ prom, origen);
        lista.add(alm);
        mostrardatos();
        controlNum.setText("");
        Nombre.setText("");
        cal1.setText("");
        cal2.setText("");
        cal3.setText("");
        origen=null;
        this.ImgLocation.setIcon(null);
        //cal4.setText("");
        
    }//GEN-LAST:event_btRegisActionPerformed
    
    public void mostrardatos(){
        String matriz[][] = new String [lista.size()][6];
        
        for(int i=0; i<lista.size(); i++){
        matriz[i][0]=lista.get(i).getCodig();
        matriz[i][1]=lista.get(i).getNombre();
        matriz[i][2]=Float.toString(lista.get(i).getCal1());
        matriz[i][3]=Float.toString(lista.get(i).getCal2());
        matriz[i][4]=Float.toString(lista.get(i).getCal3());
        //matriz[i][5]=Float.toString(lista.get(i).getCal4());
        matriz[i][5]=Float.toString(lista.get(i).getProm());
        }
        jTable1.setModel(jTable2 = new javax.swing.table.DefaultTableModel(    
            matriz,
            new String [] {
                "Num. de registro", "Nombre", "Calif 1", "Calif 2", "Calif 3", "Prom"
            }
        ));
        
    }
    
