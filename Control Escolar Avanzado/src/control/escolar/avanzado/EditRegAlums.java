//Funcion para actualizar la base de datos
    private void ActDatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ActDatActionPerformed
        try {
            FileInputStream archivofoto;
            File nombre = new File(DirIMG);
            archivofoto = new FileInputStream(nombre);
            String sql = "UPDATE alumnos SET NoControl = ?, "
                    + "Nombre = ?,"
                    + "Apellido = ?,"
                    + "Curp = ?,"
                    + "Turno = ?,"
                    + "Grupo = ?,"
                    + "Especialidad = ?,"
                    + "imagen=?"
                    + "WHERE NoControl = '" + numCont.getText() + "'";

            PreparedStatement pst = cn.prepareStatement(sql);
            pst.setString(1, numCont.getText());
            pst.setString(2, nom.getText());
            pst.setString(3, ape.getText());
            pst.setString(4, curp.getText());
            if (jRadioButton1.isSelected()) {
                pst.setString(5, "Matutino");
            } else {
                pst.setString(5, "Vespertino");
            }
            pst.setString(6, grupo.getText());
            pst.setString(7, Esp.getSelectedItem().toString());
            archivofoto = new FileInputStream(DirIMG);
            pst.setBinaryStream(8, archivofoto);

            int n = pst.executeUpdate();
            if (n > 0) {
                JOptionPane.showMessageDialog(null, "Datos actualizados con exito");

                DirIMG = null;
                LblFoto.setIcon(null);
            } else {
                JOptionPane.showMessageDialog(null, "Hubo error en el momento de actualizar los datos");
            }
        } catch (SQLException ex) {
        } catch (FileNotFoundException ex) {
        }

    }//GEN-LAST:event_ActDatActionPerformed
    //funcion para seleccionar imagen
    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        JFileChooser archivo = new JFileChooser();
        FileNameExtensionFilter filtro = new FileNameExtensionFilter("Formatos de Archivos JPEG(*.JPG;*.JPEG)", "jpg", "jpeg");
        archivo.setFileFilter(filtro);
        archivo.addChoosableFileFilter(filtro);
        archivo.setDialogTitle("Abrir Archivo");
        int ventana = archivo.showOpenDialog(null);
        if (ventana == JFileChooser.APPROVE_OPTION) {
            File file = archivo.getSelectedFile();
            DirIMG = String.valueOf(file);
            Image foto = getToolkit().getImage(String.valueOf(file));
            foto = foto.getScaledInstance(300, 300, Image.SCALE_DEFAULT);
            LblFoto.setIcon(new ImageIcon(foto));
        }
    }//GEN-LAST:event_jButton5ActionPerformed

