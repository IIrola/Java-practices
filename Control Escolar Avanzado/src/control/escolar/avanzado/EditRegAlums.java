//btn para buscar y extraer los datos solicitados en la base de datos
    private void BusRegActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BusRegActionPerformed
        //"SELECT * FROM `empleados` WHERE `NoControl` = "+noCon;

        try {

            String consultar = "SELECT NoControl, Nombre, Apellido, Curp, Imagen FROM `alumnos` WHERE `NoControl` = " + noCon.getText();

            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(consultar);

            if (rs.next()) {

                numCont.setText(rs.getString("NoControl"));
                nom.setText(rs.getString("Nombre"));
                ape.setText(rs.getString("Apellido"));
                curp.setText(rs.getString("Curp"));
                Blob blob = rs.getBlob("Imagen");

                byte[] Data = blob.getBytes(1, (int) blob.length());
                BufferedImage IMG = null;
                try {
                    IMG = ImageIO.read(new ByteArrayInputStream(Data));
                } catch (Exception e) {
                    System.out.println(e);
                }
                ImageIcon icon = new ImageIcon(IMG);
                LblFoto.setIcon(icon);
            }

        } catch (SQLException ex) {
        }

    }//GEN-LAST:event_BusRegActionPerformed
