    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String insertar = "INSERT INTO `empleados` (`NoControl`, `Nombre`, `Apellido`, `Curp`, `Turno`, `AreaTrab`, `Imagen`) VALUES (?, ?, ?, ?, ?, ?,?)";
        try {

            FileInputStream archivofoto;
            PreparedStatement pst = cn.prepareStatement(insertar);
            pst.setString(1, null);
            pst.setString(2, nombre.getText());
            pst.setString(3, apell.getText());
            pst.setString(4, Curp.getText());
            if (jRadioButton1.isSelected()) {
                pst.setString(5, "Matutino");
            } else {
                pst.setString(5, "Vespertino");
            }
            pst.setString(6, aTrab.getSelectedItem().toString());

            archivofoto = new FileInputStream(DirIMG.getText());

            pst.setBinaryStream(7, archivofoto);

            int i = pst.executeUpdate();
            if (i > 0) {
                JOptionPane.showMessageDialog(null, "Se guardo correctamente");
                limpiar();
                numeros();
                DirIMG.setText(null);
                lblfoto.setIcon(null);
            }

        } catch (SQLException ex) {
            System.out.println(ex);
        } catch (FileNotFoundException ex) {
            System.out.println(ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

