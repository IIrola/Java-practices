    void numeros() {

        int c = 0;

        String SQL = "SELECT MAX(NoControl) AS NoControl FROM empleados";

        try {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(SQL);
            if (rs.next()) {
                c = rs.getInt("NoControl");
            }
            System.out.print(c);

            if (c != 0) {
                numCont.setText(Integer.toString(c+1));
            } else {
                numCont.setText("1");
            }

        } catch (SQLException ex) {
            System.out.println(ex);
        }
    }

