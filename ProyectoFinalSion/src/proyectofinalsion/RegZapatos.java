    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String insertar = "INSERT INTO zapatos (`cod_pro`, `Modelo`, `Talla`, `Color`, `Marca`, `Imagen`, Stock, Precio) VALUES(?,?,?,?,?,?,?,?)";
        try {

            FileInputStream archivofoto;
            PreparedStatement pst = cn.prepareStatement(insertar);
            pst.setString(1, null);
            pst.setString(2, Modelo.getText());
            pst.setString(3, Talla.getValue().toString());
            pst.setString(4, Esp.getSelectedItem().toString());
            pst.setString(5, Marca.getText());
            archivofoto = new FileInputStream(DirIMG.getText());
pst.setString(7, jSpinner1.getValue().toString());
pst.setFloat(8, Float.parseFloat(jSpinner2.getValue().toString()));

            pst.setBinaryStream(6, archivofoto);

            int i = pst.executeUpdate();
            if (i > 0) {
                JOptionPane.showMessageDialog(null, "Se guardo correctamente");
                limpiar();
                DirIMG.setText(null);
                jLabel9.setIcon(null);
                numeros();
            }

        } catch (SQLException ex) {
            System.out.println(ex);
        } catch (FileNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "ERROR\nPorfavor ingrese una imagen valida");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

