    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try {
            String consultar = "SELECT User, Contra, Seccion FROM `admin` WHERE `codAD` = " + CODAD.getText();
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(consultar);
            if (rs.next()) {
                us = rs.getString("User");
                pas = rs.getString("Contra");
                sec = rs.getString("Seccion");
            }

        } catch (SQLException ex) {
            System.out.println(ex);
        }

        if (User.getText().equals(us) && Pass.getText().equals(pas)) {
            switch (sec) {
                case "Admin":
                    Principal p = new Principal();
                    this.setVisible(false);
                    p.setVisible(true);
                    break;
                case "Cajero":

                    CajeroPan b = new CajeroPan();
                    this.setVisible(false);
                    b.setVisible(true);
                    break;
                case "Almacenero":
                    AlmacenPan a = new AlmacenPan();
                    this.setVisible(false);
                    a.setVisible(true);
                    break;

            }
        } else {
            JOptionPane.showMessageDialog(null, "Acceso negado");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

