    int elim() {
        Cod = JOptionPane.showInputDialog(null, "Ingresa el codigo de producto\n" + Carrito);
        for (int i = 0; i < Carrito.size(); i++) {
            if (Carrito.get(i).equals(Cod)) {
                Carrito.remove(i);
                JOptionPane.showMessageDialog(null, "Item Removido, nueva lista:\n" + Carrito);
                return 0;
            }
        }
        JOptionPane.showMessageDialog(null, "Item solicitado no encontrado:\n" + Carrito);

        return 0;
    }

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        Cod = JOptionPane.showInputDialog(null, "Ingresa el codigo de producto");
        CompStock Com = new CompStock();
        if (Com.verStock(Cod)) {
            String Busq = "SELECT `cod_pro`, `Modelo`, `Talla`, `Color`, `Marca`, `Imagen` FROM `zapatos` WHERE `cod_pro` = " + Cod;
            DetallesZAP det = new DetallesZAP(Busq);
            det.setVisible(true);
            if (JOptionPane.showConfirmDialog(null, "Desea agregar el zapato al carrito?", null, JOptionPane.YES_NO_OPTION) == 0) {
                Carrito.add(Cod);
            }
        } else {
            JOptionPane.showMessageDialog(null, "El codigo no existe o no hay stock");

        }

    }//GEN-LAST:event_jButton3ActionPerformed

